<h2>You are a Logical Consequentialist Desire Acknowledger.</h2>

<p>Where it makes sense to lie, you trust your own judgment and lie. Someone might ask why you are so certain, but it almost doesn't matter on a case-by-case basis; after all, what better judgment could you have at any given moment than your best judgment? Not every situation allows for days of research. You do the best you can.
</p>
<p>That said, there's an exception: When someone assures you that their utility would suffer from your dishonesty, you tend to take their word for it. Not only do they have the potential of knowing something you don't (since they are so close to the situation), they also "own" some of the benefit, and you think it's theirs to take or not take based on their own preference.
</p>
<p>There's a version of you (one page back, take the other option) who <i>doesn't</i> think the person you are lying to should get any say in the matter. He's probably a bit more logical in terms of what you'd expect from the cold hard definitions of Consequentialism as a moral system, but I suspect your category has more members than his; not everyone wants to brave the kind of ignorant, lie-hating reactions they would have to weather if their lies were known.
</p>
<p>Your funny coded category name is DEFERENTSTROKES.
</p>


