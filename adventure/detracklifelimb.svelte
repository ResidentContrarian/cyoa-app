<h2>You are a Life-and-Limb Anti-Lying Conditionalist.</h2>

<p>You basically think lying is absolutely wrong, except you have some semantics-based exceptions for what "lying" is. Luckily, it's only one exception, and it mostly makes sense that you'd choose the one you did. This is a pretty easy way to resolve the whole "I'd lie, and lying is bad, but this isn't somehow" paradox you worked yourself into; if someone would be seriously endangered by you not lying, you do it. The last metroid is in captivity. The galaxy is at peace.
</p>
<p>The weakness here, and one I take seriously in the way you only can when it affects you personally, is that this is the first step to breaking the pickle-seal on becoming a consequentialist. Realistically most of us are never going to have to hide a family at risk of genocide, but all of us have to choose between our perception of consequences and following the rules all the time. Is this example really different in kind, or just in severity?</p>

<p>This also doesn't really tell us anything about whether or not you lie in <i>practice</i>. In some other branches we dig into that a little more, but it's worth noting here as well that you can have an entirely correct opinion on what moral system is best down to the finest detail of how one should live and that wouldn't really tell me much about how you actually act in real life.
</p>
