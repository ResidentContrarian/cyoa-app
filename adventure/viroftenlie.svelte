<h2>You are a Ideal-Seeking Lie-comfortable Pragmatist.</h2>

<p>You don't think lying is wrong, full-stop. If your ideal image of yourself didn't lie much you'd think that lying a lot was wrong because it took you further and further from being that guy, but your ideal image of yourself is a pragmatist who leaves themselves with a lot of flexibility to deal with situations as they come on a one-off basis.
</p>
<p>There's a passage of the Bible (the book of James) that talks a bit about how works don't grant Christian salvation, but the kind of Faith that does grant access to salvation has other implications, one of which is motivating works. Your approach to lying is sort of like that; you don't care about lying that much, but you do care about being a really top-notch guy. Some closely related categories think that lying takes you further away from that; you think that being the kind of guy who produces good outcomes is more important, and that lying helps you do that.
</p>
<p>I can hear you saying something like "this whole section is really lame and non-specific!", but I assure you that's not my fault. It turns out the whole field of virtue ethics is basically something like "good people do things that good people do!", where "good" is defined in a way much closer to <i>successful and satisfied</i>. Essentially it asks you to imagine what you want people to say at your funeral, and work backwards from there. Whether you do this by imagining an idealized version of yourself or by following in the footsteps of an impressive rolemodel Wenceslas-style is mostly left up to you.
</p>
