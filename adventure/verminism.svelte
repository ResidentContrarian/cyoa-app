<h2>You are an Ethics-of-Care Virtue Ethicist, which is basically just a Consequentialist.</h2>

<p>OK, so let's get a bit meta here.
</p>
<p>I've been digging into various moral systems for a few months now as a bit of a refresher, and of deontology, consequentialism, and virtue ethics, virtue ethics is the one I understand the least. I think that's partially by design; virtue ethics isn't really a moral system as I usually understand it. Where deontology and consequentialism both seek to define <i>good,</i> virtue ethics really doesn't. Instead of this, it tells you to find or imagine someone who is successful, and do what they'd do. Douglas Adams once created a character named Dirk Gently who would, rather than know where he was going, would find someone who was driving like they knew where <i>they</i> were going, and he'd then just follow them hoping to get the same outcomes they looked set to get. This is a LOT like that.
</p>
<p>Or rather it would be, except you chose the one side of virtue ethics that actually makes a more-than-half-hearted attempt to define virtues. The deal is that at some point feminists looked at the vaguely-defined virtues that most male philosophers picked, got pissed, and much more strictly defined a set of virtues they thought to be feminine-coded. That means that you've chosen a very specific set of virtues that all come down to nurture, care, and self sacrifice - essentially, the goodness of your actions is determined by how well they promote the good of "helping James".
</p>
<p>Fortunately or unfortunately, that means that your moral system is <i>identical in every way</i> to consequentialism. Where other forms of virtue ethicists look inwards at who they'd like to be and act based off that, you <i>say</i> you are doing that, but then tether your moral goodness entirely to someone else's outcomes, such as you can influence them.
</p>
<p>This isn't bad - for the record, I actually like this at least as much as other forms of virtue ethics. 
</p>

<p>Your funny coded category name is CONCAVE.
</p>


